import numpy as np

# Creating a numpy array
numpy_array = np.array([1, 2, 3, 4])
print(numpy_array)

# Printing the shape of the array variable called
print(numpy_array.shape)

# Printing the datatype of the elements present in the array variable
print(f'datatype of the array variable : {numpy_array.dtype}')

# Printing the size of the array variable
print(f'size of the array variable : {numpy_array.size}')